<?php 

/**
 * Somando valores literais
 */

 echo 1 + 2; // saída -> 3.
 echo 10 - 4; // saída -> 6.
 echo intdiv(7,4); // saída -> 1 essa função trunca o valor flutuante.
 echo round(7 / 4); // saída -> arredonda valores.
 echo 7 % 4; // resto da divisão. -> Usamos para verificar se um número é par ou ímpar.
 echo 7 / 0; // saída -> tende ao infinito.
 echo 4 ** 2; //saída -> 16. Exponencial.

 // Ordem de precedência de operadores -> '()' -> '**' -> '/' -> '*' -> '%' -> '+' -> '-'
